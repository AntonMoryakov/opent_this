let currentIndex = 0;
const phraseElement = document.getElementById('phrase');
const currentElement = document.getElementById('current');
const clickBtn = document.getElementById('click-btn');
const card = document.getElementById('card');

clickBtn.addEventListener('click', () => {
    if (currentIndex < phrases.length) {
        // Анимация текста
        phraseElement.classList.remove('fade-in');
        void phraseElement.offsetWidth; // Триггер рефлоу для перезапуска анимации
        
        phraseElement.innerText = phrases[currentIndex];
        phraseElement.classList.add('fade-in');
        
        currentIndex++;
        currentElement.innerText = currentIndex;

        // Если это последняя фраза, можно добавить эффект
        if (currentIndex === phrases.length) {
            clickBtn.style.display = 'none';
            card.style.borderColor = '#ff4d6d';
            card.style.borderWidth = '2px';
            card.style.borderStyle = 'solid';
        }
    }
});